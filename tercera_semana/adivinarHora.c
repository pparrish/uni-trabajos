 /*Anexar conio solo en windows*/
#ifdef _WIN32
#include <conio.h>
#endif

#include <stdio.h>

void main(){
  int hour,
      min,
      sec;
  /*Entrada de datos*/
  printf("teclee  la hora como h:m:s \n");
  scanf("%d:%d:%d", &hour, &min, &sec);

  
  /*salida de datos*/
  if(hour <= 24 && hour >= 0 &&  min <= 60 && min >= 0 && sec <= 60 && sec >= 0 )
    printf("hora correcta\n");
  else
    printf("hora incorrecta\n");

  #ifdef _WIN32
  getch();
  #endif

  return;

}
